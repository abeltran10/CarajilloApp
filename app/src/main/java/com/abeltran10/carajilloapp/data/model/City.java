package com.abeltran10.carajilloapp.data.model;

public class City {

    private String id;

    private String name;



    public City() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
